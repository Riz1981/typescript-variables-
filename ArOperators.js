var num1 = 10;
var num2 = 5;
var result = num1 + num2; //Addition Operator +
console.log("Addition Operator :" + result);
result = num1 - num2; //Subtraction Operator -
console.log("Subtraction Operator -:" + result);
result = num1 * num2; //Multiplication Operator *
console.log("Multiplication Operator *:" + result);
result = num1 / num2; //Division Operator /
console.log("Division Operator /:" + result);
result = num1 % num2; //Remainer/Modulus Operator %
console.log("Remainer/Modulus Operator %:" + result);
result = Math.pow(num1, num2); //Exponent Operator **
console.log("Exponent Operator **:" + result);
